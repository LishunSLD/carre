const carte1 = document.querySelector("#carte1").querySelector(".carte")
const back1 = document.querySelector("#carte1").querySelector(".back")
carte1.addEventListener("click", () => {
    carte1.classList.toggle("d-none")
    back1.classList.toggle("d-none")
  })
back1.addEventListener("click", () => {
    carte1.classList.toggle("d-none")
    back1.classList.toggle("d-none")
  })

